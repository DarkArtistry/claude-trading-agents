const STRATEGY_TOOLS: Anthropic.Tool[] = [
  TOOL_SCHEMAS.get_klines!,
  TOOL_SCHEMAS.get_indicators!,
  TOOL_SCHEMAS.get_ticker!,
];

const EXECUTION_TOOLS: Anthropic.Tool[] = [
  TOOL_SCHEMAS.place_order!,
  TOOL_SCHEMAS.cancel_order!,
  TOOL_SCHEMAS.get_ticker!,
];

export interface SubAgentDeps {
  client: Anthropic;
  ops: Ops;
  model: string;
  logger: Logger;
}

export class SubAgents {
  constructor(private deps: SubAgentDeps) {}

  delegateToRisk(task: string, context?: Record<string, unknown>): Promise<string> {
    return this.run({ system: RISK_SYSTEM, tools: RISK_TOOLS, role: "risk" }, task, context);
  }

  delegateToStrategy(task: string, context?: Record<string, unknown>): Promise<string> {
    return this.run(
      { system: STRATEGY_SYSTEM, tools: STRATEGY_TOOLS, role: "strategy" },
      task,
      context,
    );
  }

  delegateToExecution(task: string, context?: Record<string, unknown>): Promise<string> {
    return this.run(
      { system: EXECUTION_SYSTEM, tools: EXECUTION_TOOLS, role: "execution" },
      task,
      context,
    );
  }

  private async run(
    def: { system: string; tools: Anthropic.Tool[]; role: string },
    task: string,
    context: Record<string, unknown> | undefined,
  ): Promise<string> {
    const userText = context ? `${task}\n\nContext:\n${JSON.stringify(context, null, 2)}` : task;
    const history: Anthropic.MessageParam[] = [{ role: "user", content: userText }];

    for (let turn = 0; turn < 8; turn++) {
      const res = await this.deps.client.messages.create({
        model: this.deps.model,
        max_tokens: 2048,
        system: def.system,
        tools: def.tools,
        messages: history,
      });

      history.push({ role: "assistant", content: res.content });

      const toolUses = res.content.filter(
        (b): b is Anthropic.ToolUseBlock => b.type === "tool_use",
      );
      if (res.stop_reason !== "tool_use" || toolUses.length === 0) {
        const text = res.content
          .filter((b): b is Anthropic.TextBlock => b.type === "text")
          .map((b) => b.text)
          .join("\n")
          .trim();
        return text;
      }

      const results = await Promise.all(
        toolUses.map(async (t) => {
          const result = await this.executeTool(t.name, t.input as Record<string, unknown>);
          return {
            type: "tool_result" as const,
            tool_use_id: t.id,
            content: JSON.stringify(result),
          };
        }),
      );
      history.push({ role: "user", content: results });
    }
    this.deps.logger.warn("sub-agent hit turn limit", { role: def.role });
    return JSON.stringify({ error: "sub-agent exceeded turn limit" });
  }

  private async executeTool(name: string, input: Record<string, unknown>): Promise<unknown> {
    const o = this.deps.ops;
    switch (name) {
      case "get_portfolio_summary": return o.getPortfolioSummary();
      case "get_positions": return o.getPositions();
      case "get_ticker": return o.getTicker(input.symbol as string);
      case "get_klines":
        return o.getKlines(input.symbol as string, input.timeframe as string, input.limit as number | undefined);
      case "get_indicators":
        return o.getIndicators(input.symbol as string, input.timeframe as string);
      case "check_risk_limits":
        return o.checkRiskLimits(
          input.symbol as string,
          input.side as "buy" | "sell",
          input.desiredNotionalQuote as number,
        );
      case "place_order":
        return o.placeOrder(input as unknown as Parameters<Ops["placeOrder"]>[0]);
      case "cancel_order":
        return o.cancelOrder(input.orderId as string, input.symbol as string);
      default:
        return { error: `unknown tool: ${name}` };
    }
  }
}
